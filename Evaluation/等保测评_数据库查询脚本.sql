-- ############################################################################
--                          一、MySQL 数据库
-- ############################################################################

-- ============================================================================
-- 1.1 查询数据库版本
-- 对应测评项：系统基本信息收集
-- ============================================================================
show variables where variable_name like '%version%';

-- ============================================================================
-- 1.2 查询用户标识唯一性
-- 对应测评项：身份鉴别 - 用户标识唯一
-- 说明：检查是否存在重复用户名，确认每个用户有唯一标识
-- ============================================================================
select user, host FROM mysql.user;

-- ============================================================================
-- 1.3 查询口令复杂度策略
-- 对应测评项：身份鉴别 - 口令复杂度
-- 说明：检查是否启用密码验证插件（validate_password）
-- ============================================================================
show variables like 'validate%';

-- ============================================================================
-- 1.4 查询口令有效期
-- 对应测评项：身份鉴别 - 口令有效期
-- 说明：检查密码过期策略，默认密码生存周期
-- ============================================================================
show variables like 'default_password_lifetime';

-- ============================================================================
-- 1.5 查询登录失败处理功能
-- 对应测评项：身份鉴别 - 登录失败处理
-- 说明：检查连接控制插件和最大连接错误次数限制
-- ============================================================================
show variables like '%connection_control%';
show variables like 'max_connect_errors';

-- ============================================================================
-- 1.6 查询操作超时自动退出
-- 对应测评项：身份鉴别 - 超时自动退出
-- 说明：检查各类超时设置，确认空闲会话是否自动断开
-- ============================================================================
show variables like '%timeout%';

-- ============================================================================
-- 1.7 查询SSL/TLS加密支持
-- 对应测评项：通信完整性 - 传输加密
-- 说明：检查数据库是否启用SSL加密通信
-- ============================================================================
show variables like "%have_ssl%";
show variables like "%have_openssl%";
show status like 'ssl_cipher';

-- ============================================================================
-- 1.8 查询用户权限
-- 对应测评项：访问控制 - 权限分配
-- 说明：查看特定用户（如root）的权限授予情况
-- ============================================================================
SHOW grants for 'root'@'localhost';

-- ============================================================================
-- 1.9 查看所有用户
-- 对应测评项：身份鉴别 - 用户标识唯一
-- 说明：列出所有数据库用户及其主机范围
-- ============================================================================
SELECT user,host FROM mysql.user;
SELECT user,account_locked from user;

-- ============================================================================
-- 1.10 查看用户权限表
-- 对应测评项：访问控制 - 权限管理
-- 说明：查看用户、数据库、表、列级别的权限配置
-- ============================================================================
select * from mysql.user;
select * from mysql.db;
select * from mysql.tables_priv;
select * from mysql.columns_priv;

-- ============================================================================
-- 1.11 查看审计日志
-- 对应测评项：安全审计 - 审计记录
-- 说明：检查通用日志、二进制日志、慢查询日志是否开启
-- ============================================================================
show global variables like '%general%';
show variables like '%log_bin%';
show variables like '%slow_query_log%';

-- ============================================================================
-- 1.12 查看已安装插件
-- 对应测评项：系统安全配置
-- 说明：检查数据库安装了哪些安全相关插件
-- ============================================================================
show plugins;

-- ============================================================================
-- 1.13 查看用户登录IP限制
-- 对应测评项：访问控制 - 访问控制策略
-- 说明：检查root用户的远程登录权限和IP限制
-- ============================================================================
show grants for root@localhost;


-- ############################################################################
--                          二、Oracle 数据库
-- ############################################################################

-- ============================================================================
-- 2.1 查询数据库版本
-- 对应测评项：系统基本信息收集
-- ============================================================================
select * from v$version;

-- ============================================================================
-- 2.2 查询用户标识唯一性
-- 对应测评项：身份鉴别 - 用户标识唯一
-- 说明：查看所有数据库用户的唯一标识、创建时间和账户状态
-- ============================================================================
select username,user_id,created,account_status,profile from dba_users;

-- ============================================================================
-- 2.3 查询密码复杂度策略
-- 对应测评项：身份鉴别 - 口令复杂度
-- 说明：检查密码验证函数是否配置
-- ============================================================================
select * from dba_profiles where resource_name='PASSWORD_VERIFY_FUNCTION';

-- ============================================================================
-- 2.4 查询口令有效期
-- 对应测评项：身份鉴别 - 口令有效期
-- 说明：检查默认配置文件的密码生存周期
-- ============================================================================
SELECT * FROM DBA_PROFILES WHERE PROFILE='DEFAULT' AND RESOURCE_NAME='PASSWORD_LIFE_TIME';

-- ============================================================================
-- 2.5 查询登录失败处理和超时策略（方法一）
-- 对应测评项：身份鉴别 - 登录失败处理 / 超时退出
-- 说明：查看所有配置文件的安全策略参数
-- ============================================================================
select lower(limit),lower(resource_name) from dba_profiles;

-- ============================================================================
-- 2.6 查询登录失败次数限制
-- 对应测评项：身份鉴别 - 登录失败处理
-- 说明：检查账户被锁定前允许的失败登录次数
-- ============================================================================
SELECT LIMIT FROM DBA_PROFILES WHERE PROFILE='DEFAULT' AND RESOURCE_NAME='FAILED_LOGIN_ATTEMPTS';

-- ============================================================================
-- 2.7 查询用户锁定时间
-- 对应测评项：身份鉴别 - 登录失败处理
-- 说明：检查登录失败后账户被锁定的持续时间
-- ============================================================================
SELECT * FROM DBA_PROFILES WHERE PROFILE='DEFAULT' AND RESOURCE_NAME='PASSWORD_LOCK_TIME';

-- ============================================================================
-- 2.8 查询用户超时退出
-- 对应测评项：身份鉴别 - 超时自动退出
-- 说明：检查空闲会话超时设置
-- ============================================================================
SELECT LIMIT FROM DBA_PROFILES WHERE PROFILE='DEFAULT' AND RESOURCE_NAME='IDLE_TIME';

-- ============================================================================
-- 2.9 查询审计功能
-- 对应测评项：安全审计 - 审计功能
-- 说明：检查Oracle审计跟踪是否启用
-- ============================================================================
show parameter audit_trail;

-- ============================================================================
-- 2.10 查询审计日志
-- 对应测评项：安全审计 - 审计记录
-- 说明：查看审计日志记录内容
-- ============================================================================
select * from aud$;

-- ============================================================================
-- 2.11 查询默认账户
-- 对应测评项：身份鉴别 - 默认账户管理
-- 说明：检查是否存在处于开放状态的默认账户
-- ============================================================================
select username,user_id from dba_users where account_status='OPEN';


-- ############################################################################
--                          三、SQL Server 数据库
-- ############################################################################

-- ============================================================================
-- 3.1 查询数据库版本
-- 对应测评项：系统基本信息收集
-- ============================================================================
SELECT @@VERSION;

-- ============================================================================
-- 3.2 查询登录用户
-- 对应测评项：身份鉴别 - 用户标识唯一
-- 说明：查看所有登录账户信息
-- ============================================================================
select * from syslogins;

-- ============================================================================
-- 3.3 查询登录用户信息及权限
-- 对应测评项：访问控制 - 权限分配
-- 说明：查看登录用户的详细信息和权限映射
-- ============================================================================
sp_helplogins;

-- ============================================================================
-- 3.4 查看SQL Server身份认证模式的登录账户
-- 对应测评项：身份鉴别 - 身份鉴别方式
-- 说明：查看SQL身份验证和Windows身份验证的登录账户
-- ============================================================================
select * from sys.sql_logins;
select * from sys.syslogins;

-- ============================================================================
-- 3.5 查询配置参数（含C2审计）
-- 对应测评项：安全审计 - 审计功能
-- 说明：查看所有服务器配置参数，包括C2审计跟踪设置
-- ============================================================================
sp_configure;

-- ============================================================================
-- 3.6 查询连接加密状态
-- 对应测评项：通信完整性 - 传输加密
-- 说明：检查当前会话是否启用加密连接
-- ============================================================================
SELECT session_id, encrypt_option FROM sys.dm_exec_connections WHERE session_id = @@SPID;


-- ############################################################################
--                          四、达梦数据库
-- ############################################################################

-- ============================================================================
-- 4.1 查询数据库版本
-- 对应测评项：系统基本信息收集
-- ============================================================================
select * from V$version;

-- ============================================================================
-- 4.2 查看用户以及口令信息
-- 对应测评项：身份鉴别 - 用户管理
-- 说明：查看所有数据库用户及其密码信息
-- ============================================================================
SELECT USERNAME, PASSWORD FROM DBA_USERS;

-- ============================================================================
-- 4.3 查询口令策略
-- 对应测评项：身份鉴别 - 口令复杂度
-- 说明：检查密码策略配置参数
-- ============================================================================
SELECT * FROM V$PARAMETER WHERE NAME= 'PWD_POLICY';

-- ============================================================================
-- 4.4 查询登录失败锁定策略
-- 对应测评项：身份鉴别 - 登录失败处理
-- 说明：查看各用户的登录失败次数限制和锁定时间
-- ============================================================================
select b.username as "达梦数据库用户名", a.failed_num as "失败次数限制", a.failed_attemps as "失败尝试次数", a.lock_time as "锁定时间(min)" from sysusers a right join all_users b on a.id=b.user_id;

-- ============================================================================
-- 4.5 查询登录连接超时及自动退出
-- 对应测评项：身份鉴别 - 超时自动退出
-- 说明：检查连接时间和空闲超时设置
-- ============================================================================
SELECT PROFILE, RESOURCE_NAME, LIMIT FROM DBA_PROFILES WHERE RESOURCE_NAME IN ('CONNECT_TIME', 'CONNECT_IDLE_TIME');

-- ============================================================================
-- 4.6 查看所有用户
-- 对应测评项：身份鉴别 - 用户标识唯一
-- 说明：列出所有数据库用户
-- ============================================================================
SELECT USERNAME FROM DBA_USERS;

-- ============================================================================
-- 4.7 查看系统权限
-- 对应测评项：访问控制 - 权限分配
-- 说明：查看系统级权限授予情况
-- ============================================================================
SELECT * FROM DBA_SYS_PRIVS ORDER BY GRANTEE;

-- ============================================================================
-- 4.8 查询审计开关
-- 对应测评项：安全审计 - 审计功能
-- 说明：检查达梦数据库审计功能是否启用
-- ============================================================================
SELECT * FROM V$DM_INI WHERE PARA_NAME='ENABLE_AUDIT';

-- ============================================================================
-- 4.9 查看日志记录
-- 对应测评项：安全审计 - 审计记录
-- 说明：查看审计日志记录内容
-- ============================================================================
select * from V$AUDITRECORDS;

-- ============================================================================
-- 4.10 SSL协议加密
-- 对应测评项：通信完整性 - 传输加密
-- 说明：检查是否启用SSL协议加密通信
-- ============================================================================
SELECT * FROM V$PARAMETER WHERE NAME='ENABLE_ENCRYPT';


