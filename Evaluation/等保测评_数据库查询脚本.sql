-- ============================================================================
-- 等保测评_数据库查询脚本.sql
-- 用途：整合各类型数据库的等保测评抓包查询命令
-- 说明：按数据库类型分类，每个命令前有中文注释说明用途和对应等保测评项
-- 使用方法：根据实际数据库类型，执行对应章节的SQL命令
