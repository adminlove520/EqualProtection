-- ============================================================================
-- 等保测评_MySQL查询.sql
-- 用途：等保测评数据库安全检查查询脚本（MySQL专用）
-- 适用数据库类型：MySQL
-- 对应等保测评项概览：
--   - 系统基本信息收集（数据库版本）
--   - 身份鉴别（用户标识唯一性、口令复杂度、口令有效期、登录失败处理、超时自动退出）
--   - 通信完整性（SSL/TLS加密支持）
--   - 访问控制（权限分配、访问控制策略）
--   - 安全审计（审计日志、插件管理）
-- 使用方法：
--   1. 使用 MySQL 客户端工具（如命令行、管理工具等）连接到目标数据库
--   2. 根据测评需要，逐条或批量执行本文件中的SQL命令
--   3. 每条命令前有中文注释说明用途和对应等保测评项，请结合实际环境判断结果
--   4. 部分命令可能需要管理员权限（DBA/SA/SYSDBA等）才能正常执行
-- 注意事项：
--   - 请在测试环境验证后再在生产环境执行
--   - 审计相关查询可能涉及敏感数据，请注意数据保护
-- ============================================================================

-- ############################################################################
--                          一、MySQL 数据库
-- ############################################################################

-- ============================================================================
-- 1.1 查询数据库版本
-- 对应测评项：系统基本信息收集
-- ============================================================================
show variables where variable_name like '%version%';

-- ============================================================================
-- 1.2 查询用户标识唯一性
-- 对应测评项：身份鉴别 - 用户标识唯一
-- 说明：检查是否存在重复用户名，确认每个用户有唯一标识
-- ============================================================================
select user, host FROM mysql.user;

-- ============================================================================
-- 1.3 查询口令复杂度策略
-- 对应测评项：身份鉴别 - 口令复杂度
-- 说明：检查是否启用密码验证插件（validate_password）
-- ============================================================================
show variables like 'validate%';

-- ============================================================================
-- 1.4 查询口令有效期
-- 对应测评项：身份鉴别 - 口令有效期
-- 说明：检查密码过期策略，默认密码生存周期
-- ============================================================================
show variables like 'default_password_lifetime';

-- ============================================================================
-- 1.5 查询登录失败处理功能
-- 对应测评项：身份鉴别 - 登录失败处理
-- 说明：检查连接控制插件和最大连接错误次数限制
-- ============================================================================
show variables like '%connection_control%';
show variables like 'max_connect_errors';

-- ============================================================================
-- 1.6 查询操作超时自动退出
-- 对应测评项：身份鉴别 - 超时自动退出
-- 说明：检查各类超时设置，确认空闲会话是否自动断开
-- ============================================================================
show variables like '%timeout%';

-- ============================================================================
-- 1.7 查询SSL/TLS加密支持
-- 对应测评项：通信完整性 - 传输加密
-- 说明：检查数据库是否启用SSL加密通信
-- ============================================================================
show variables like "%have_ssl%";
show variables like "%have_openssl%";
show status like 'ssl_cipher';

-- ============================================================================
-- 1.8 查询用户权限
-- 对应测评项：访问控制 - 权限分配
-- 说明：查看特定用户（如root）的权限授予情况
-- ============================================================================
SHOW grants for 'root'@'localhost';

-- ============================================================================
-- 1.9 查看所有用户
-- 对应测评项：身份鉴别 - 用户标识唯一
-- 说明：列出所有数据库用户及其主机范围
-- ============================================================================
SELECT user,host FROM mysql.user;
SELECT user,account_locked from user;

-- ============================================================================
-- 1.10 查看用户权限表
-- 对应测评项：访问控制 - 权限管理
-- 说明：查看用户、数据库、表、列级别的权限配置
-- ============================================================================
select * from mysql.user;
select * from mysql.db;
select * from mysql.tables_priv;
select * from mysql.columns_priv;

-- ============================================================================
-- 1.11 查看审计日志
-- 对应测评项：安全审计 - 审计记录
-- 说明：检查通用日志、二进制日志、慢查询日志是否开启
-- ============================================================================
show global variables like '%general%';
show variables like '%log_bin%';
show variables like '%slow_query_log%';

-- ============================================================================
-- 1.12 查看已安装插件
-- 对应测评项：系统安全配置
-- 说明：检查数据库安装了哪些安全相关插件
-- ============================================================================
show plugins;

-- ============================================================================
-- 1.13 查看用户登录IP限制
-- 对应测评项：访问控制 - 访问控制策略
-- 说明：检查root用户的远程登录权限和IP限制
-- ============================================================================
show grants for root@localhost;