-- ============================================================================
-- 3.1 查询数据库版本
-- 对应测评项：系统基本信息收集
-- ============================================================================
SELECT @@VERSION;

-- ============================================================================
-- 3.2 查询登录用户
-- 对应测评项：身份鉴别 - 用户标识唯一
-- 说明：查看所有登录账户信息
-- ============================================================================
select * from syslogins;

-- ============================================================================
-- 3.3 查询登录用户信息及权限
-- 对应测评项：访问控制 - 权限分配
-- 说明：查看登录用户的详细信息和权限映射
-- ============================================================================
sp_helplogins;

-- ============================================================================
-- 3.4 查看SQL Server身份认证模式的登录账户
-- 对应测评项：身份鉴别 - 身份鉴别方式
-- 说明：查看SQL身份验证和Windows身份验证的登录账户
-- ============================================================================
select * from sys.sql_logins;
select * from sys.syslogins;

-- ============================================================================
-- 3.5 查询配置参数（含C2审计）
-- 对应测评项：安全审计 - 审计功能
-- 说明：查看所有服务器配置参数，包括C2审计跟踪设置
-- ============================================================================
sp_configure;

-- ============================================================================
-- 3.6 查询连接加密状态
-- 对应测评项：通信完整性 - 传输加密
-- 说明：检查当前会话是否启用加密连接
-- ============================================================================
SELECT session_id, encrypt_option FROM sys.dm_exec_connections WHERE session_id = @@SPID;