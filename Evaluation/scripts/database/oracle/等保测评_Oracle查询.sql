-- ============================================================================
-- 2.1 查询数据库版本
-- 对应测评项：系统基本信息收集
-- ============================================================================
select * from v$version;

-- ============================================================================
-- 2.2 查询用户标识唯一性
-- 对应测评项：身份鉴别 - 用户标识唯一
-- 说明：查看所有数据库用户的唯一标识、创建时间和账户状态
-- ============================================================================
select username,user_id,created,account_status,profile from dba_users;

-- ============================================================================
-- 2.3 查询密码复杂度策略
-- 对应测评项：身份鉴别 - 口令复杂度
-- 说明：检查密码验证函数是否配置
-- ============================================================================
select * from dba_profiles where resource_name='PASSWORD_VERIFY_FUNCTION';

-- ============================================================================
-- 2.4 查询口令有效期
-- 对应测评项：身份鉴别 - 口令有效期
-- 说明：检查默认配置文件的密码生存周期
-- ============================================================================
SELECT * FROM DBA_PROFILES WHERE PROFILE='DEFAULT' AND RESOURCE_NAME='PASSWORD_LIFE_TIME';

-- ============================================================================
-- 2.5 查询登录失败处理和超时策略（方法一）
-- 对应测评项：身份鉴别 - 登录失败处理 / 超时退出
-- 说明：查看所有配置文件的安全策略参数
-- ============================================================================
select lower(limit),lower(resource_name) from dba_profiles;

-- ============================================================================
-- 2.6 查询登录失败次数限制
-- 对应测评项：身份鉴别 - 登录失败处理
-- 说明：检查账户被锁定前允许的失败登录次数
-- ============================================================================
SELECT LIMIT FROM DBA_PROFILES WHERE PROFILE='DEFAULT' AND RESOURCE_NAME='FAILED_LOGIN_ATTEMPTS';

-- ============================================================================
-- 2.7 查询用户锁定时间
-- 对应测评项：身份鉴别 - 登录失败处理
-- 说明：检查登录失败后账户被锁定的持续时间
-- ============================================================================
SELECT * FROM DBA_PROFILES WHERE PROFILE='DEFAULT' AND RESOURCE_NAME='PASSWORD_LOCK_TIME';

-- ============================================================================
-- 2.8 查询用户超时退出
-- 对应测评项：身份鉴别 - 超时自动退出
-- 说明：检查空闲会话超时设置
-- ============================================================================
SELECT LIMIT FROM DBA_PROFILES WHERE PROFILE='DEFAULT' AND RESOURCE_NAME='IDLE_TIME';

-- ============================================================================
-- 2.9 查询审计功能
-- 对应测评项：安全审计 - 审计功能
-- 说明：检查Oracle审计跟踪是否启用
-- ============================================================================
show parameter audit_trail;

-- ============================================================================
-- 2.10 查询审计日志
-- 对应测评项：安全审计 - 审计记录
-- 说明：查看审计日志记录内容
-- ============================================================================
select * from aud$;

-- ============================================================================
-- 2.11 查询默认账户
-- 对应测评项：身份鉴别 - 默认账户管理
-- 说明：检查是否存在处于开放状态的默认账户
-- ============================================================================
select username,user_id from dba_users where account_status='OPEN';