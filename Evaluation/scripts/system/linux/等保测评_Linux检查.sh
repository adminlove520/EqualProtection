#!/bin/bash
# ============================================================================
# 等保测评_Linux服务器检查脚本.sh
# 用途：Linux操作系统等保测评安全检查
# 适用系统：CentOS/RHEL/Ubuntu/Debian/龙蜥等Linux发行版
# 使用方法：以root权限执行 bash 等保测评_Linux检查.sh
# 对应测评项：身份鉴别、访问控制、安全审计、入侵检测
# ============================================================================

echo "============================================================"
echo "       等保测评 - Linux服务器安全检查脚本"
echo "       执行时间: $(date '+%Y-%m-%d %H:%M:%S')"
echo "============================================================"


# ============================================================================
# 0. 系统基本信息
# 对应测评项：系统基本信息收集
# 说明：收集操作系统版本、网络配置、系统时间等基本信息
# ============================================================================

echo ""
echo "[系统基本信息] 正在收集系统基本信息..."

# 查看操作系统发行版信息
cat /etc/*release*

# 查看网络接口配置和IP地址
ifconfig

# 查看当前系统时间（用于确认时间同步状态）
date

echo ""


# ============================================================================
# 一、身份鉴别
# ============================================================================

echo "[身份鉴别] 正在检查身份鉴别相关配置..."

# ----------------------------------------------------------------------------
# 1.1 用户信息检查
# 对应测评项：身份鉴别 - 用户标识唯一
# 说明：检查系统用户账户信息，确认用户标识唯一性
# ----------------------------------------------------------------------------

# 查看所有用户账户信息
cat /etc/passwd

# 查看用户密码哈希（需要root权限）
cat /etc/shadow

# 查看用户组信息
cat /etc/group

# 检查shadow文件权限（应仅root可读）
ls -l /etc/shadow

# 检查passwd文件权限
ls -l /etc/passwd

# ----------------------------------------------------------------------------
# 1.2 密码策略检查
# 对应测评项：身份鉴别 - 口令复杂度 / 口令有效期
# 说明：检查PAM认证配置和密码质量策略
# ----------------------------------------------------------------------------

# 查看PAM系统认证配置（含密码复杂度策略）
cat /etc/pam.d/system-auth

# 查看PAM密码认证配置
cat /etc/pam.d/password-auth

# 查看PAM通用认证配置（Ubuntu/Debian）
cat /etc/pam.d/common-auth

# 查看密码质量配置（密码长度、复杂度等）
cat /etc/security/pwquality.conf

# 查看密码过期策略（密码有效期、最小长度等）
cat /etc/login.defs

# ----------------------------------------------------------------------------
# 1.3 root账户信息检查
# 对应测评项：身份鉴别 - 特权账户管理
# 说明：检查root账户的密码策略和过期信息
# ----------------------------------------------------------------------------

# 查看root账户密码老化信息
chage -l root

# 查看root账户密码状态
passwd -S root

# ----------------------------------------------------------------------------
# 1.4 SSH安全配置检查
# 对应测评项：身份鉴别 - 远程身份鉴别 / 安全通信
# 说明：检查SSH服务的安全配置
# ----------------------------------------------------------------------------

# 查看SSH服务的PAM认证配置
cat /etc/pam.d/sshd

# 查看SSH服务运行状态
service sshd status

# 查看SSH端口监听状态
netstat -ano | grep 22

# 检查是否允许root远程登录（建议设为no）
cat /etc/ssh/sshd_config | grep PermitRootLogin

# ----------------------------------------------------------------------------
# 1.5 超时自动退出检查
# 对应测评项：身份鉴别 - 超时自动退出
# 说明：检查操作超时后是否自动退出登录会话
# ----------------------------------------------------------------------------

# 查看系统全局配置文件（含TMOUT超时设置）
cat /etc/profile

# 查看当前命令历史记录条数
echo $HISTSIZE

# 检查是否配置了TMOUT超时变量
cat /etc/profile | grep TMOUT

# 检查是否配置了HISTSIZE历史记录限制
cat /etc/profile | grep HISTSIZE

# ----------------------------------------------------------------------------
# 1.6 龙蜥（Anolis OS）登录失败策略检查
# 对应测评项：身份鉴别 - 登录失败处理
# 说明：检查龙蜥系统特有的pam_faillock登录失败锁定配置
# ----------------------------------------------------------------------------

# 搜索PAM faillock模块配置（登录失败锁定功能）
grep -r "pam_faillock" /etc/pam.d/

echo ""


# ============================================================================
# 二、访问控制
# ============================================================================

echo "[访问控制] 正在检查访问控制相关配置..."

# ----------------------------------------------------------------------------
# 2.1 sudo权限配置检查
# 对应测评项：访问控制 - 权限分配 / 最小权限原则
# 说明：检查sudoers文件中的权限分配情况
# ----------------------------------------------------------------------------

# 查看sudo权限配置（需要root权限）
cat /etc/sudoers

echo ""


# ============================================================================
# 三、安全审计
# ============================================================================

echo "[安全审计] 正在检查安全审计相关配置..."

# ----------------------------------------------------------------------------
# 3.1 SELinux安全状态
# 对应测评项：安全审计 - 访问控制增强
# 说明：检查SELinux是否启用及运行模式
# ----------------------------------------------------------------------------

# 查看SELinux当前状态
sestatus

# ----------------------------------------------------------------------------
# 3.2 日志服务状态检查
# 对应测评项：安全审计 - 审计记录
# 说明：检查系统日志服务和审计服务的运行状态
# ----------------------------------------------------------------------------

# 查看rsyslog系统日志服务状态
service rsyslog status

# 查看auditd审计服务状态
service auditd status

# 查看rsyslog日志服务配置
cat /etc/rsyslog.conf

# 查看系统日志最新内容
cat /var/log/messages

# 查看审计日志最新记录
head -10 /var/log/audit/audit.log

echo ""


# ============================================================================
# 四、入侵检测
# ============================================================================

echo "[入侵检测] 正在检查入侵防范相关配置..."

# ----------------------------------------------------------------------------
# 4.1 服务和端口检查
# 对应测评项：入侵防范 - 端口与服务管理
# 说明：检查系统开启的服务和监听端口，识别不必要的服务
# ----------------------------------------------------------------------------

# 查看所有已启用的系统服务
systemctl list-unit-files | grep enabled

# 查看所有已安装的RPM软件包
rpm -aq

# 查看所有已安装的YUM软件包
yum list installed

# 查看当前正在运行的服务
systemctl list-units --type=service

# 查看所有网络连接和监听端口
netstat -na

# 查看TCP/UDP连接及对应进程
netstat -tuanp

# 查看TCP监听端口及对应进程
netstat -ntlp

# ----------------------------------------------------------------------------
# 4.2 防火墙配置检查
# 对应测评项：入侵防范 - 网络边界防护 / 访问控制
# 说明：检查防火墙规则和访问控制策略
# ----------------------------------------------------------------------------

# 查看TCP Wrappers拒绝规则
cat /etc/hosts.deny

# 查看TCP Wrappers允许规则
cat /etc/hosts.allow

# 查看iptables防火墙规则
iptables -L

# 查看firewalld防火墙策略（CentOS 7+）
firewall-cmd --list-all

echo ""
echo "============================================================"
echo ">>> Linux 服务器安全检查完成。"
echo "============================================================"
