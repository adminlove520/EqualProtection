#!/bin/bash
# ============================================================================
# 等保测评_系统检查脚本.sh
# 用途：整合Linux和Windows操作系统的等保测评检查命令
# 说明：按检查类别分类，每个命令前有中文注释说明用途和对应等保测评项
# 使用方法：
#   Linux：直接以root权限执行 bash 等保测评_系统检查脚本.sh
#   Windows：手动在CMD/PowerShell中逐条执行Windows部分的命令
# ============================================================================

echo "============================================================"
echo "       等保测评 - 操作系统安全检查脚本"
echo "       执行时间: $(date '+%Y-%m-%d %H:%M:%S')"
echo "============================================================"


# ############################################################################
#                          第一部分：Linux 服务器检查
# ############################################################################

echo ""
echo ">>> 开始 Linux 服务器安全检查..."
echo ""

# ============================================================================
# 0. 系统基本信息
# 对应测评项：系统基本信息收集
# 说明：收集操作系统版本、网络配置、系统时间等基本信息
# ============================================================================

echo "[系统基本信息] 正在收集系统基本信息..."

# 查看操作系统发行版信息
cat /etc/*release*

# 查看网络接口配置和IP地址
ifconfig

# 查看当前系统时间（用于确认时间同步状态）
date

echo ""


# ============================================================================
# 一、身份鉴别
# ============================================================================

echo "[身份鉴别] 正在检查身份鉴别相关配置..."

# ----------------------------------------------------------------------------
# 1.1 用户信息检查
# 对应测评项：身份鉴别 - 用户标识唯一
# 说明：检查系统用户账户信息，确认用户标识唯一性
# ----------------------------------------------------------------------------

# 查看所有用户账户信息
cat /etc/passwd

# 查看用户密码哈希（需要root权限）
cat /etc/shadow

# 查看用户组信息
cat /etc/group

# 检查shadow文件权限（应仅root可读）
ll /etc/shadow

# 检查passwd文件权限
ll /etc/passwd

# ----------------------------------------------------------------------------
# 1.2 密码策略检查
# 对应测评项：身份鉴别 - 口令复杂度 / 口令有效期
# 说明：检查PAM认证配置和密码质量策略
# ----------------------------------------------------------------------------

# 查看PAM系统认证配置（含密码复杂度策略）
cat /etc/pam.d/system-auth

# 查看PAM密码认证配置
cat /etc/pam.d/password-auth

# 查看PAM通用认证配置（Ubuntu/Debian）
cat /etc/pam.d/common-auth

# 查看密码质量配置（密码长度、复杂度等）
cat /etc/security/pwquality.conf

# 查看密码过期策略（密码有效期、最小长度等）
cat /etc/login.defs

# ----------------------------------------------------------------------------
# 1.3 root账户信息检查
# 对应测评项：身份鉴别 - 特权账户管理
# 说明：检查root账户的密码策略和过期信息
# ----------------------------------------------------------------------------

# 查看root账户密码老化信息
chage -l root

# 查看root账户密码状态
passwd -S root

# ----------------------------------------------------------------------------
# 1.4 SSH安全配置检查
# 对应测评项：身份鉴别 - 远程身份鉴别 / 安全通信
# 说明：检查SSH服务的安全配置
# ----------------------------------------------------------------------------

# 查看SSH服务的PAM认证配置
cat /etc/pam.d/sshd

# 查看SSH服务运行状态
service sshd status

# 查看SSH端口监听状态
netstat -ano | grep 22

# 检查是否允许root远程登录（建议设为no）
cat /etc/ssh/sshd_config | grep PermitRootLogin

# ----------------------------------------------------------------------------
# 1.5 超时自动退出检查
# 对应测评项：身份鉴别 - 超时自动退出
# 说明：检查操作超时后是否自动退出登录会话
# ----------------------------------------------------------------------------

# 查看系统全局配置文件（含TMOUT超时设置）
cat /etc/profile

# 查看当前命令历史记录条数
echo $HISTSIZE

# 检查是否配置了TMOUT超时变量
cat /etc/profile | grep TMOUT

# 检查是否配置了HISTSIZE历史记录限制
cat /etc/profile | grep HISTSIZE

# ----------------------------------------------------------------------------
# 1.6 龙蜥（Anolis OS）登录失败策略检查
# 对应测评项：身份鉴别 - 登录失败处理
# 说明：检查龙蜥系统特有的pam_faillock登录失败锁定配置
# ----------------------------------------------------------------------------

# 搜索PAM faillock模块配置（登录失败锁定功能）
grep -r "pam_faillock" /etc/pam.d/

echo ""


# ============================================================================
# 二、访问控制
# ============================================================================

echo "[访问控制] 正在检查访问控制相关配置..."

# ----------------------------------------------------------------------------
# 2.1 sudo权限配置检查
# 对应测评项：访问控制 - 权限分配 / 最小权限原则
# 说明：检查sudoers文件中的权限分配情况
# ----------------------------------------------------------------------------

# 查看sudo权限配置（需要root权限）
cat /etc/sudoers

echo ""


# ============================================================================
# 三、安全审计
# ============================================================================

echo "[安全审计] 正在检查安全审计相关配置..."

# ----------------------------------------------------------------------------
# 3.1 SELinux安全状态
# 对应测评项：安全审计 - 访问控制增强
# 说明：检查SELinux是否启用及运行模式
# ----------------------------------------------------------------------------

# 查看SELinux当前状态
sestatus

# ----------------------------------------------------------------------------
# 3.2 日志服务状态检查
# 对应测评项：安全审计 - 审计记录
# 说明：检查系统日志服务和审计服务的运行状态
# ----------------------------------------------------------------------------

# 查看rsyslog系统日志服务状态
service rsyslog status

# 查看auditd审计服务状态
service auditd status

# 查看rsyslog日志服务配置
cat /etc/rsyslog.conf

# 查看系统日志最新内容
cat /var/log/messages

# 查看审计日志最新记录
head -10 /var/log/audit/audit.log

echo ""


# ============================================================================
# 四、入侵检测
# ============================================================================

echo "[入侵检测] 正在检查入侵防范相关配置..."

# ----------------------------------------------------------------------------
# 4.1 服务和端口检查
# 对应测评项：入侵防范 - 端口与服务管理
# 说明：检查系统开启的服务和监听端口，识别不必要的服务
# ----------------------------------------------------------------------------

# 查看所有已启用的系统服务
systemctl list-unit-files | grep enabled

# 查看所有已安装的RPM软件包
rpm -aq

# 查看所有已安装的YUM软件包
yum list installed

# 查看当前正在运行的服务
systemctl list-units --type=service

# 查看所有网络连接和监听端口
netstat -na

# 查看TCP/UDP连接及对应进程
netstat -tuanp

# 查看TCP监听端口及对应进程
netstat -ntlp

# ----------------------------------------------------------------------------
# 4.2 防火墙配置检查
# 对应测评项：入侵防范 - 网络边界防护 / 访问控制
# 说明：检查防火墙规则和访问控制策略
# ----------------------------------------------------------------------------

# 查看TCP Wrappers拒绝规则
cat /etc/hosts.deny

# 查看TCP Wrappers允许规则
cat /etc/hosts.allow

# 查看iptables防火墙规则
iptables -L

# 查看firewalld防火墙策略（CentOS 7+）
firewall-cmd --list-all

echo ""
echo ">>> Linux 服务器安全检查完成。"
echo ""


# ############################################################################
#                          第二部分：Windows 操作系统检查
# ############################################################################

echo "============================================================"
echo ">>> 以下为 Windows 操作系统检查命令"
echo ">>> 请在 Windows CMD 或 PowerShell 中逐条执行"
echo "============================================================"
echo ""

# ============================================================================
# 0. 系统基本信息
# 对应测评项：系统基本信息收集
# 说明：收集操作系统版本、补丁级别等基本信息
# ============================================================================

echo "[系统基本信息] 请在Windows中执行以下命令："
echo "  systeminfo"
echo "  wmic os get Caption,CSDVersion,BuildNumber"
echo ""

# ============================================================================
# 一、身份鉴别
# ============================================================================

echo "[身份鉴别] 请在Windows中执行以下命令或操作："
echo ""

# ----------------------------------------------------------------------------
# 1.1 用户唯一标识检查
# 对应测评项：身份鉴别 - 用户标识唯一
# 说明：查看所有本地用户的名称和SID，确认用户标识唯一性
# ----------------------------------------------------------------------------
echo "  -- 用户唯一标识 --"
echo "  wmic useraccount get name,sid"

# ----------------------------------------------------------------------------
# 1.2 密码策略检查
# 对应测评项：身份鉴别 - 口令复杂度 / 口令长度 / 口令有效期
# 说明：通过本地安全策略GUI检查密码策略配置
# ----------------------------------------------------------------------------
echo "  -- 密码策略（通过GUI检查）--"
echo "  运行 secpol.msc -> 本地安全策略 -> 账户策略 -> 密码策略"
echo "  检查项：密码必须符合复杂性要求、密码最小长度、密码最短/最长使用期限"

# ----------------------------------------------------------------------------
# 1.3 账户锁定策略检查
# 对应测评项：身份鉴别 - 登录失败处理
# 说明：通过本地安全策略GUI检查账户锁定策略
# ----------------------------------------------------------------------------
echo "  -- 账户锁定策略（通过GUI检查）--"
echo "  运行 secpol.msc -> 本地安全策略 -> 账户策略 -> 账户锁定策略"
echo "  检查项：账户锁定持续时间、账户锁定阈值、重置账户锁定计数器的时间间隔"

# ----------------------------------------------------------------------------
# 1.4 屏幕保护（超时自动退出）检查
# 对应测评项：身份鉴别 - 超时自动退出
# 说明：通过GUI检查屏幕保护程序是否启用并设置了密码保护
# ----------------------------------------------------------------------------
echo "  -- 屏幕保护/超时退出（通过GUI检查）--"
echo "  个性化 -> 屏幕保护程序 -> 启用并在等待时间设置超时值、勾选\"在恢复时显示登录屏幕\""

# ----------------------------------------------------------------------------
# 1.5 远程会话超时检查
# 对应测评项：身份鉴别 - 超时自动退出
# 说明：通过组策略GUI检查远程桌面会话时间限制
# ----------------------------------------------------------------------------
echo "  -- 远程会话超时（通过GUI检查）--"
echo "  运行 gpedit.msc -> 计算机配置 -> 管理模板 -> Windows组件 -> 远程桌面服务 -> 会话时间限制"
echo "  检查项：设置已断开会话的时间限制、活动但空闲的会话的时间限制"

echo ""

# ============================================================================
# 二、访问控制
# ============================================================================

echo "[访问控制] 请在Windows中执行以下命令或操作："
echo ""

# ----------------------------------------------------------------------------
# 2.1 本地用户和组管理检查
# 对应测评项：访问控制 - 用户管理
# 说明：通过GUI检查本地用户和组的配置
# ----------------------------------------------------------------------------
echo "  -- 本地用户和组（通过GUI检查）--"
echo "  运行 lusrmgr.msc -> 本地用户和组"
echo "  检查项：是否存在多余账户、Guest账户状态、用户组成员"

# ----------------------------------------------------------------------------
# 2.2 用户权限分配检查
# 对应测评项：访问控制 - 权限分配 / 最小权限原则
# 说明：通过本地安全策略GUI检查用户权限分配
# ----------------------------------------------------------------------------
echo "  -- 用户权限分配（通过GUI检查）--"
echo "  运行 secpol.msc -> 本地安全策略 -> 本地策略 -> 用户权限分配"
echo "  检查项：从网络访问此计算机、本地登录、关闭系统等权限分配"

# ----------------------------------------------------------------------------
# 2.3 命令行查看用户和组
# 对应测评项：访问控制 - 用户管理
# 说明：通过命令行快速查看本地用户和组
# ----------------------------------------------------------------------------
echo "  -- 命令行查看用户和组 --"
echo "  net user"
echo "  net localgroup"

echo ""

# ============================================================================
# 三、安全审计
# ============================================================================

echo "[安全审计] 请在Windows中执行以下命令或操作："
echo ""

# ----------------------------------------------------------------------------
# 3.1 审核策略检查
# 对应测评项：安全审计 - 审计策略
# 说明：通过本地安全策略GUI检查审核策略配置
# ----------------------------------------------------------------------------
echo "  -- 审核策略（通过GUI检查）--"
echo "  运行 secpol.msc -> 本地安全策略 -> 本地策略 -> 审核策略"
echo "  检查项：审核登录事件、审核对象访问、审核策略更改、审核账户管理等"

# ----------------------------------------------------------------------------
# 3.2 事件日志查看
# 对应测评项：安全审计 - 审计记录
# 说明：通过命令行和GUI查看安全事件日志
# ----------------------------------------------------------------------------
echo "  -- 事件日志查看 --"
echo "  事件查看器GUI: 运行 eventvwr.msc"
echo "  命令行列出所有日志: wevtutil el"
echo "  查看最近5条安全日志: wevtutil qe Security /c:5 /rd:true /f:text"

echo ""

# ============================================================================
# 四、入侵检测
# ============================================================================

echo "[入侵检测] 请在Windows中执行以下命令或操作："
echo ""

# ----------------------------------------------------------------------------
# 4.1 已安装程序检查
# 对应测评项：入侵防范 - 软件管理
# 说明：查看系统中已安装的程序，识别不必要的软件
# ----------------------------------------------------------------------------
echo "  -- 已安装程序 --"
echo "  wmic product get name,version"

# ----------------------------------------------------------------------------
# 4.2 网络端口检查
# 对应测评项：入侵防范 - 端口与服务管理
# 说明：查看所有网络连接和监听端口
# ----------------------------------------------------------------------------
echo "  -- 网络端口 --"
echo "  netstat -ano"

# ----------------------------------------------------------------------------
# 4.3 默认共享检查
# 对应测评项：入侵防范 - 共享安全
# 说明：检查系统默认共享是否关闭
# ----------------------------------------------------------------------------
echo "  -- 默认共享 --"
echo "  net share"

# ----------------------------------------------------------------------------
# 4.4 防火墙配置检查
# 对应测评项：入侵防范 - 网络边界防护
# 说明：检查Windows防火墙各配置文件的启用状态和规则
# ----------------------------------------------------------------------------
echo "  -- 防火墙配置 --"
echo "  防火墙GUI: 运行 firewall.cpl"
echo "  命令行查看所有配置文件: netsh advfirewall show allprofiles"

echo ""
echo "============================================================"
echo ">>> Windows 操作系统检查命令列表输出完成。"
echo ">>> 请在目标 Windows 系统上逐条执行上述命令。"
echo "============================================================"
